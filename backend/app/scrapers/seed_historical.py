async def seed():
    await init_db()
    count = 0
    async with AsyncSessionLocal() as db:
        # --- Fertilizantes ---
        # FERT_RAW cols: [yyyy, mm, urea_wld, urea_arg, dap_wld, dap_arg, map_wld, map_arg, potash_wld]
        for row in FERT_RAW:
            y, m = row[0], row[1]
            d = date(y, m, 1)
            await upsert_price(db, "urea",   "world_cfr", d, row[2], SOURCE)
            await upsert_price(db, "urea",   "arg_fca",   d, row[3], SOURCE)
            await upsert_price(db, "dap",    "world_fob", d, row[4], SOURCE)
            await upsert_price(db, "dap",    "arg_fca",   d, row[5], SOURCE)
            await upsert_price(db, "map",    "world_cfr", d, row[6], SOURCE)
            await upsert_price(db, "map",    "arg_fca",   d, row[7], SOURCE)
            await upsert_price(db, "potash", "world_cfr", d, row[8], SOURCE)
            count += 7

        # --- Granos ---
        # GRAIN_RAW cols: [yyyy, mm, soy_cbot, soy_bcr, corn_cbot, corn_bcr, wheat_cbot, wheat_bcr]
        for row in GRAIN_RAW:
            y, m = row[0], row[1]
            d = date(y, m, 1)
            await upsert_price(db, "soy",   "cbot",    d, row[2], SOURCE)
            await upsert_price(db, "soy",   "bcr_fas", d, row[3], SOURCE)
            await upsert_price(db, "corn",  "cbot",    d, row[4], SOURCE)
            await upsert_price(db, "corn",  "bcr_fas", d, row[5], SOURCE)
            await upsert_price(db, "wheat", "cbot",    d, row[6], SOURCE)
            await upsert_price(db, "wheat", "bcr_fas", d, row[7], SOURCE)
            count += 6

    print(f"Seed completado: {count} registros insertados/actualizados.")
    print(f"  Fertilizantes: {len(FERT_RAW)} meses × 7 series = {len(FERT_RAW)*7} registros")
    print(f"  Granos:        {len(GRAIN_RAW)} meses × 6 series = {len(GRAIN_RAW)*6} registros")


if __name__ == "__main__":
    asyncio.run(seed())
